#include <stdio.h>
void OctSum(int x, int y)
{
	printf("=\n");
	printf("%o\n", x + y);
}
void OctMinus(int x, int y)
{
	printf("=\n");
	printf("%o\n", x - y);
}
void OctMul(int x, int y)
{
	printf("=\n");
	printf("%o\n", x * y);
}
void OctDiv(int x, int y)
{
	printf("=\n");
	printf("%o\n", x / y);
}